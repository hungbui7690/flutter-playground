/*
  Type Annotations


*/

void main() {
  String name = "mario"; // type annotation
  name = "luigi";
  print(name);

  const int age = 22; // const with type annotation
  print(age);

  bool isOpen = true;
  print(isOpen);

  double height = 1.8;
  print(height);

  int? point; // nullable
  print(point);
}
